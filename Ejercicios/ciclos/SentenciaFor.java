package ciclos;
/*
 * Imprimmir los numeros del 1-10 de forma secuencialmete.
*/
public class SentenciaFor {
    public static void main(String[] args) {
        // Incremento
        for(int i=1; i<=10; i++) {
            System.out.println(i);
        }
        // Decremento
        for(int i=10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
